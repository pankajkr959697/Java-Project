    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //update product
        try{
            String name=n.getText();
            String pp=p.getText();
            String qq=q.getText();
            String status=(String)s.getSelectedItem();
            if(!name.equals("") && !pp.equals("") && !qq.equals("")){
                int price=Integer.parseInt(pp);
                int qty=Integer.parseInt(qq);
                db.DbConnect.st.executeUpdate("update product set price="+price+",qty="+qty+",status='"+status+"' where name='"+name+"'");
                JOptionPane.showMessageDialog(null, "success");
                n.setText(null);
                p.setText(null);
                q.setText(null);
                getData();
            }else{
                JOptionPane.showMessageDialog(null, "Plz fill the complete form");
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // delete product
        int x=JOptionPane.showConfirmDialog(null,"Do you really wanna delete?","Confirmation", JOptionPane.YES_NO_OPTION);
        if(x==JOptionPane.YES_OPTION){
            try{
                String name=n.getText();
                if(!name.equals("")){
                    db.DbConnect.st.executeUpdate("delete from product where name='"+name+"'");
                    JOptionPane.showMessageDialog(null, "success");
                    n.setText(null);
                    p.setText(null);
                    q.setText(null);
                    getData();
                }else{
                    JOptionPane.showMessageDialog(null, "Plz fill the complete form");
                }
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex);
            }
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

